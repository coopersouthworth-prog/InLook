from math import pi
def radar_chart(fundamental,
                technical,
                risk):

    categories = [

        "Fundamental",

        "Technical",

        "Risk"

    ]

    values = [

        fundamental,

        technical,

        risk

    ]

    values += values[:1]

    angles = [

        n/float(len(categories))*2*pi

        for n in range(len(categories))

    ]

    angles += angles[:1]

    plt.figure(figsize=(7,7))

    ax = plt.subplot(111,polar=True)

    ax.plot(

        angles,

        values,

        linewidth=3

    )

    ax.fill(

        angles,

        values,

        alpha=.25

    )

    ax.set_xticks(angles[:-1])

    ax.set_xticklabels(categories)

    ax.set_ylim(0,100)

    plt.title("Portfolio Score Radar")

    plt.show()

