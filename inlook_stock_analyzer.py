def score_dataframe(scores):

    df = pd.DataFrame({

        "Metric":list(scores.keys()),

        "Score":list(scores.values())

    })

    df["Weight"] = df["Metric"].map(WEIGHTS)

    df["Weighted Score"] = df["Score"] * df["Weight"]

    df["Max"] = 10

    return df

def weighted_total(df):

    weighted = df["Weighted Score"].sum()

    maximum = (df["Weight"]*10).sum()

    percentage = weighted/maximum*100

    return weighted,maximum,percentage

