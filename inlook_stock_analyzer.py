def download_benchmark():

    benchmark_df = yf.download(
        BENCHMARK,
        period=LOOKBACK,
        progress=False
    )

    # If MultiIndex columns are present, flatten them. For single ticker, yfinance can return (Metric, Ticker)
    if isinstance(benchmark_df.columns, pd.MultiIndex):
        # Assume the structure is (Metric, Ticker) and extract the Metric level
        benchmark_df.columns = benchmark_df.columns.get_level_values(0)

    # Prioritize 'Adj Close' as it's the most adjusted price.
    if 'Adj Close' in benchmark_df.columns:
        benchmark_df['Close'] = benchmark_df['Adj Close']
    elif 'Close' in benchmark_df.columns:
        # If 'Adj Close' is not there, use 'Close' which should already be correctly named
        pass
    elif 'close' in benchmark_df.columns:
        benchmark_df = benchmark_df.rename(columns={'close': 'Close'})
    else:
        # If neither 'Adj Close' nor 'Close' (or 'close') is found, raise an error.
        raise ValueError("Could not find 'Close' or 'Adj Close' column in benchmark data.")

    # Keep only the 'Close' column for simplicity in calculations.
    benchmark_df = benchmark_df[['Close']]

    # Sanity check: ensure 'Close' column is of numeric type
    if 'Close' in benchmark_df.columns:
        benchmark_df['Close'] = pd.to_numeric(benchmark_df['Close'], errors='coerce')
        benchmark_df = benchmark_df.dropna(subset=['Close']) # Drop rows where Close price couldn't be converted

    return benchmark_df

def calculate_indicators(history):

    history = history.copy()

    history["MA20"] = history["Close"].rolling(20).mean()

    history["MA50"] = history["Close"].rolling(50).mean()

    history["MA200"] = history["Close"].rolling(200).mean()

    rsi = RSIIndicator(history["Close"],window=14)

    history["RSI"] = rsi.rsi()

    macd = MACD(history["Close"])

    history["MACD"] = macd.macd()

    history["Signal"] = macd.macd_signal()

    history["Histogram"] = macd.macd_diff()

    return history

def calculate_risk(history):

    returns = history["Close"].pct_change().dropna()

    annual_return = returns.mean()*TRADING_DAYS

    volatility = returns.std()*np.sqrt(TRADING_DAYS)

    sharpe = annual_return/volatility if volatility !=0 else 0

    cumulative = (1+returns).cumprod()

    running_max = cumulative.cummax()

    drawdown = cumulative/running_max-1

    max_drawdown = drawdown.min()

    return {

        "Annual Return":annual_return,

        "Volatility":volatility,

        "Sharpe Ratio":sharpe,

        "Max Drawdown":max_drawdown

    }

def benchmark_statistics(stock_history,benchmark_history):

    stock = calculate_risk(stock_history)

    benchmark = calculate_risk(benchmark_history)

    comparison = {

        "Return Difference":
            stock["Annual Return"]-
            benchmark["Annual Return"],

        "Sharpe Difference":
            stock["Sharpe Ratio"]-
            benchmark["Sharpe Ratio"],

        "Volatility Difference":
            stock["Volatility"]-
            benchmark["Volatility"]

    }

    return stock,benchmark,comparison

