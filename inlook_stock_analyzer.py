def score_fundamentals(metrics):

    scores = {}

    scores["Revenue Growth"] = score_metric(
        metrics["Revenue Growth"],
        FUNDAMENTAL_RULES["Revenue Growth"]
    )

    scores["Profit Margin"] = score_metric(
        metrics["Profit Margin"],
        FUNDAMENTAL_RULES["Profit Margin"]
    )

    scores["Return on Equity"] = score_metric(
        metrics["Return on Equity"],
        FUNDAMENTAL_RULES["Return on Equity"]
    )

    scores["Earnings Growth"] = score_metric(
        metrics["Earnings Growth"],
        FUNDAMENTAL_RULES["Earnings Growth"]
    )

    debt = metrics["Debt to Equity"]

    if debt is None or pd.isna(debt):
        scores["Debt to Equity"] = 0

    elif debt < 30:
        scores["Debt to Equity"] = 10

    elif debt < 60:
        scores["Debt to Equity"] = 8

    elif debt < 100:
        scores["Debt to Equity"] = 5

    elif debt < 200:
        scores["Debt to Equity"] = 2

    else:
        scores["Debt to Equity"] = 0

    scores["Current Ratio"] = score_metric(
        metrics["Current Ratio"],
        FUNDAMENTAL_RULES["Current Ratio"]
    )

    return scores

def score_technicals(history):

    scores = {}

    price = history["Close"].iloc[-1]

    ma50 = history["MA50"].iloc[-1]

    ma200 = history["MA200"].iloc[-1]

    rsi = history["RSI"].iloc[-1]

    macd = history["MACD"].iloc[-1]

    signal = history["Signal"].iloc[-1]

    scores["Golden Cross"] = 8 if ma50 > ma200 else 0

    scores["Above MA50"] = 5 if price > ma50 else 0

    scores["Above MA200"] = 5 if price > ma200 else 0

    if 40 <= rsi <= 60:
        scores["RSI"] = 4

    elif 30 <= rsi < 40 or 60 < rsi <= 70:
        scores["RSI"] = 3

    elif rsi < 30:
        scores["RSI"] = 2

    else:
        scores["RSI"] = 0

    scores["MACD"] = 3 if macd > signal else 0

    return scores

