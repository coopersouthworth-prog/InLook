def score_metric(value, thresholds, reverse=False):

    if value is None:
        return 0

    if pd.isna(value):
        return 0

    if reverse:

        thresholds = sorted(thresholds,key=lambda x:x[0])

        for maximum,score in thresholds:

            if value <= maximum:
                return score

        return 0

    else:

        thresholds = sorted(thresholds,key=lambda x:x[0],reverse=True)

        for minimum,score in thresholds:

            if value >= minimum:
                return score

        return 0

def gauge(score,max_score=100,length=30):

    filled = int(score/max_score*length)

    empty = length-filled

    return "█"*filled+"░"*empty

def confidence_score(metrics):

    available = 0

    total = len(metrics)

    for value in metrics.values():

        if value is not None:

            if not pd.isna(value):

                available += 1

    confidence = available/total*100

    return confidence

def download_stock(ticker):

    stock = yf.Ticker(ticker)

